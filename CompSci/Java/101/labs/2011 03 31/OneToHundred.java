//    This program will print the numbers 1 to 100 and print a new line every ten
//    digits.
//********************************************************************************
class OneToHundred
{
    //------------------------------------------------------------------------
    //    CLASS CONSTANTS
    //------------------------------------------------------------------------
    
    //------------------------------------------------------------------------
    //    Instance Variables
    //------------------------------------------------------------------------
    
    //------------------------------------------------------------------------
    //    This is the main method
    //------------------------------------------------------------------------
    public static void main(String [] args)
    {
        //------------------------------------------------------------------------
        //    CONSTANTS
        //------------------------------------------------------------------------
        final int LINE = 10;
        final int LIMIT = 100;

        //------------------------------------------------------------------------
        //    Variables
        //------------------------------------------------------------------------
        int count;

        //------------------------------------------------------------------------
        //    Run count through a for loop printing out count as the program loops.
        //    Loop until LIMIT[100] is reached. For every multiple of 10, indent to
        //    a new line.
        //------------------------------------------------------------------------
        for(count = 1; count <= LIMIT;count++){
            System.out.print(count + "\t");
            if(count % LINE == 0)
                System.out.println();
        }
    }
}