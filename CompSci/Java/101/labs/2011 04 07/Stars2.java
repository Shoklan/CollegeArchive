//	This program will output a line of 10 stars. It will then print 9
//	stars on a new line. And, it will continue with that pattern until
//	the last row prints 1 star
//********************************************************************

class Stars2
{
	//----------------------------------------------------------------
	//	CLASS CONSTANTS
	//----------------------------------------------------------------
	
	//----------------------------------------------------------------
	//	Instance Variables
	//----------------------------------------------------------------
	
	//----------------------------------------------------------------
	//	This is the main method
	//----------------------------------------------------------------
	public static void main(String [] args)
	{
		//----------------------------------------------------------------
		//	CONSTANTS
		//----------------------------------------------------------------
		final int MAX_ROWS = 10;
	
		//----------------------------------------------------------------
		//	Variables
		//----------------------------------------------------------------

		for(int rows = 1; rows <= MAX_ROWS; rows++)
		{
			for(int stars = 10; stars >= rows; stars--)
			{
				System.out.print("*");
			}
			
			System.out.println();
		}
	}
}